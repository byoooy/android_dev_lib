package com.example.sample.bean;

public class BaseBean {
	private String data;
	private String info;
	private int code;

	public String getData() {
		return data;
	}

	public void setData(String data) {
		this.data = data;
	}

	public String getInfo() {
		return info;
	}

	public void setInfo(String info) {
		this.info = info;
	}

	public int getCode() {
		return code;
	}

	public void setCode(int code) {
		this.code = code;
	}

}
